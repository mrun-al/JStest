// String interpolation is a process of constructing a new string by inserting  variables within a string literal.
const name = "Mrunal";
const greeting = `Hello, ${name}!`;
console.log(greeting);
// String concatenation is the process of combining two or more strings into a single string.By using the ' + ' operator
const firstName = "Mrunal";
const lastName = "Ubale";
const fullName = firstName + " " + lastName;
console.log(fullName);